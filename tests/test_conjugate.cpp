TEST(ConjugationTests, GuessPredicateRoot)
{
	for (int row = 0; row < (sizeof(s_conjugationTestData) / sizeof(s_conjugationTestData[0])); row++)
	{
		//const wchar_t* pszRootForm = s_conjugationTestData[row][0];
		std::wstring expectedRoot = s_conjugationTestData[row][0];
		
		for (int c = 1; c < 9; c++)
		{
			std::vector<std::wstring> toks;
			boost::split(toks, s_conjugationTestData[row][c], boost::is_any_of(L","));

			for (auto tok : toks)
			{
				if (tok.back() == L'X')
					continue;

				if (tok.back() == L'O')
				{
					tok.pop_back();
				}

				std::vector<PosInfo*> vec = g_posdic.MatchPredicate(CharString(tok.c_str()));
				if (vec.empty())
				{
					WidePrintf(L"Match(%ls) failed: no predicate guessed.\n", tok.c_str());
					EXPECT_FALSE(vec.empty());
					break;
				}

				EXPECT_EQ(vec.front()->GetString(), expectedRoot);
				if (vec.front()->GetString() != expectedRoot)
				{
					WidePrintf(L"%ls: Guessed(%ls) != Expected(%ls)\n", tok.c_str(), vec.front()->GetString().c_str(), expectedRoot.c_str());
					return;
				}
			}
		}
	}
}

